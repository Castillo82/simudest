package com.simudest.simudest.exception;

public final class GrupoNotFoundException extends Exception {

    public GrupoNotFoundException() {
        super();
    }

    public GrupoNotFoundException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public GrupoNotFoundException(final String message) {
        super(message);
    }

    public GrupoNotFoundException(final Throwable cause) {
        super(cause);
    }

}
