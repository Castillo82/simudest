package com.simudest.simudest.exception;

public final class ProvinciaNotFoundException extends Exception {

    public ProvinciaNotFoundException() {
        super();
    }

    public ProvinciaNotFoundException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public ProvinciaNotFoundException(final String message) {
        super(message);
    }

    public ProvinciaNotFoundException(final Throwable cause) {
        super(cause);
    }

}
