package com.simudest.simudest.exception;

public final class EleccionNotFoundException extends Exception {

    public EleccionNotFoundException() {
        super();
    }

    public EleccionNotFoundException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public EleccionNotFoundException(final String message) {
        super(message);
    }

    public EleccionNotFoundException(final Throwable cause) {
        super(cause);
    }

}
