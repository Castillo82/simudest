package com.simudest.simudest.exception;

public final class PlazaNotFoundException extends Exception {

    public PlazaNotFoundException() {
        super();
    }

    public PlazaNotFoundException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public PlazaNotFoundException(final String message) {
        super(message);
    }

    public PlazaNotFoundException(final Throwable cause) {
        super(cause);
    }

}
