package com.simudest.simudest.exception;

public final class EspecialidadNotFoundException extends Exception {

    public EspecialidadNotFoundException() {
        super();
    }

    public EspecialidadNotFoundException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public EspecialidadNotFoundException(final String message) {
        super(message);
    }

    public EspecialidadNotFoundException(final Throwable cause) {
        super(cause);
    }

}
