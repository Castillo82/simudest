package com.simudest.simudest.exception;

public final class OpositorNotFoundException extends Exception {

    public OpositorNotFoundException() {
        super();
    }

    public OpositorNotFoundException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public OpositorNotFoundException(final String message) {
        super(message);
    }

    public OpositorNotFoundException(final Throwable cause) {
        super(cause);
    }

}
