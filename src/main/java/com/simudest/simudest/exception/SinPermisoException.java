package com.simudest.simudest.exception;

public final class SinPermisoException extends Exception {

    public SinPermisoException() {
        super();
    }

    public SinPermisoException(final String message, final Throwable cause) {
        super(message, cause);
    }

    public SinPermisoException(final String message) {
        super(message);
    }

    public SinPermisoException(final Throwable cause) {
        super(cause);
    }

}
